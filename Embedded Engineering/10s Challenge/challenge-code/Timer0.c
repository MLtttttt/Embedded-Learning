#include <reg52.h>

void Timer0_init(void) { //@11.0592MHz 1ms
    TMOD &= 0xF0;        // 清零定时器0的模式位（保留定时器1的设置）
    TMOD |= 0x01;        // 设置定时器0为模式1（16位定时器）
    TH0 = 0xFC;          // 设置初值高位
    TL0 = 0x66;          // 设置初值低位
    ET0 = 1;             // 开启定时器0中断
    EA  = 1;             // 开启全局中断
    TR0 = 1;             // 启动定时器0
}